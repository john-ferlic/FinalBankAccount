package com.BankAccount;


public class Income {
	private int ssnNumber;
	private String modeIncome;
	private int annual;
	private String house;
	private int rent;
	
	public int getSsnNumber() {
		return ssnNumber;
	}
	public void setSsnNumber(int ssnNumber) {
		this.ssnNumber = ssnNumber;
	}
	public String getModeIncome() {
		return modeIncome;
	}
	public void setModeIncome(String modeIncome) {
		this.modeIncome = modeIncome;
	}
	public int getAnnual() {
		return annual;
	}
	public void setAnnual(int annual) {
		this.annual = annual;
	}
	public String getHouse() {
		return house;
	}
	public void setHouse(String house) {
		this.house = house;
	}
	public int getRent() {
		return rent;
	}
	public void setRent(int rent) {
		this.rent = rent;
	}
	
	
	
}


